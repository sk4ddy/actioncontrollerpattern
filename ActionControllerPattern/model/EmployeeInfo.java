package model;

public class EmployeeInfo {

	private String name;
	private String surname;
	private String dateOfBirth;
	private String placeOfBirth;
	private String title;
	private String lengthOfService;
	private String vacationDays;
	private String leftVacationDays;
	private String sickDays;
	private String leftSickDays;
	private String address;
	private String contactNumber;
	private String salary;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSurname() {
		return surname;
	}
	public void setSurname(String surname) {
		this.surname = surname;
	}
	public String getDob() {
		return dateOfBirth;
	}
	public void setDob(String dob) {
		this.dateOfBirth = dob;
	}
	public String getPlaceOfBirth() {
		return placeOfBirth;
	}
	public void setPlaceOfBirth(String placeOfBirth) {
		this.placeOfBirth = placeOfBirth;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLengthOfService() {
		return lengthOfService;
	}
	public void setLengthOfService(String lengthOfService) {
		this.lengthOfService = lengthOfService;
	}
	public String getVacationDays() {
		return vacationDays;
	}
	public void setVacationDays(String vacationDays) {
		this.vacationDays = vacationDays;
	}
	public String getLeftVacationDays() {
		return leftVacationDays;
	}
	public void setLeftVacationDays(String leftVacationDays) {
		this.leftVacationDays = leftVacationDays;
	}
	public String getSickDays() {
		return sickDays;
	}
	public void setSickDays(String sickDays) {
		this.sickDays = sickDays;
	}
	public String getLeftSickDays() {
		return leftSickDays;
	}
	public void setLeftSickDays(String leftSickDays) {
		this.leftSickDays = leftSickDays;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getContactNumber() {
		return contactNumber;
	}
	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}
	public String getSalary() {
		return salary;
	}
	public void setSalary(String salary) {
		this.salary = salary;
	}

}
